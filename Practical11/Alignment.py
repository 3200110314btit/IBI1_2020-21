def matrix(seq1,seq2):
    edit_distance = 0
    for i in range (len(seq1)):
        if seq1[i] != '\n':
            x = amino.index(seq1[i])
            y = amino.index(seq2[i])
            edit_distance = edit_distance + blosum[x][y]
    return edit_distance

def per(seq1,seq2):
    z = 0
    for i in range (len(seq1)):
        if seq1[i] == seq2[i]:
            z = z + 1
    per = z / len(seq1) *100
